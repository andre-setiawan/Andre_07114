package Entity;

public class andre07114_KelasEntity {
    public String nama, code;

    public andre07114_KelasEntity(String nama, String code) {
        this.nama   = nama;
        this.code = code;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
